extern "C" {

    int get_font_index(char c) {
        if (c >= 'A' && c <= 'Z') return (c - 'A' + 1);
        if (c >= 'a' && c <= 'z') return (c - 'a' + 1);
        if (c >= '0' && c <= '9') return (c - '0' + 29); // Der Clou: Rechnet 0-9 direkt auf 29-38
        if (c == ':') return 27;
        if (c == '-') return 28;
        if (c == ' ') return 0;
        if (c == '_') return 39;
        return 0;
    }

    void draw_rectangle(unsigned int* fb, int stride, int x, int y, int w, int h, unsigned int color) {
        if (!fb) return;
        for (int i = 0; i < h; i++) {
            for (int j = 0; j < w; j++) {
                int target_x = x + j;
                int target_y = y + i;
                if (target_x >= 0 && target_x < 1024 && target_y >= 0 && target_y < 768)
                    fb[target_y * stride + target_x] = color;
            }
        }
    }

    void draw_char(unsigned int* fb, int stride, int x, int y, char c, unsigned int color) {
        int font_idx = get_font_index(c);
        for (int i = 0; i < 8; i++) {
            unsigned char line = font_data[font_idx][i];
            for (int j = 0; j < 8; j++) {
                if (line & (0x80 >> j)) {
                    int tx = x + j;
                    int ty = y + i;
                    if (tx >= 0 && tx < 1024 && ty >= 0 && ty < 768)
                        fb[ty * stride + tx] = color;
                }
            }
        }
    }

    void draw_string(unsigned int* fb, int stride, int x, int y, const char* str, unsigned int color) {
        if (!str) return;
        for (int i = 0; str[i] != '\0'; i++) {
            draw_char(fb, stride, x + (i * 8), y, str[i], color);
        }
    }

    void draw_window(unsigned int* fb, int stride, int x, int y, int w, int h, const char* title, unsigned int color) {
        draw_rectangle(fb, stride, x + 4, y + 4, w, h, CLR_SHADOW);
        draw_rectangle(fb, stride, x, y, w, h, color);
        draw_rectangle(fb, stride, x, y, w, 25, CLR_SELECT);
        draw_string(fb, stride, x + 8, y + 6, title, CLR_WHITE);
        // Schlie▀en Button (X) - nur Optik
        draw_rectangle(fb, stride, x + w - 22, y + 3, 18, 18, 0x00A00000);
        draw_char(fb, stride, x + w - 17, y + 8, 'X', CLR_WHITE);
    }

    void draw_desktop(unsigned int* fb, int stride, int width, int height) {
        draw_rectangle(fb, stride, 0, 0, width, height, CLR_DESKTOP);

        // Taskleiste
        int bar_h = 40;
        draw_rectangle(fb, stride, 0, height - bar_h, width, bar_h, CLR_MENU);
        draw_rectangle(fb, stride, 0, height - bar_h, width, 1, CLR_WHITE);

        // Start Button
        draw_rectangle(fb, stride, 5, height - 35, 80, 30, CLR_SELECT);
        draw_string(fb, stride, 15, height - 27, "START", CLR_WHITE);
    }

    void wm_init(const SyscallTable* sys) {
        if (!sys || !sys->fb_ptr) return;
        draw_desktop(sys->fb_ptr, sys->stride, 1024, 768);
    }
}